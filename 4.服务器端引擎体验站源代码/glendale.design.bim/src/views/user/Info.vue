<template>
  <a-row>
    <a-col :span="12" :offset="6">
      <a-card>
        <div class="page-title">个人信息</div>
        <a-form-model ref="ruleForm" :model="form" :rules="rules" :label-col="{ span: 8 }" :wrapper-col="{ span: 8 }">
          <a-form-model-item label="账号" v-show="false">
            <span>{{ form.userName }}</span>
          </a-form-model-item>
          <a-form-model-item label="姓名" prop="name">
            <a-input placeholder="请输入" v-model="form.name" />
          </a-form-model-item>
          <a-form-model-item label="绑定手机号" prop="phoneNumber">
            <a-input-search placeholder="请输入手机号" @search="onSend" v-model="form.phoneNumber">
              <a-button slot="enterButton" style="padding:5px;width:120px" :disabled="smsButton">{{ smsText }}</a-button>
            </a-input-search>
          </a-form-model-item>
          <a-form-model-item label="验证码" prop="code">
            <a-input placeholder="请输入验证码" v-model="form.code" />
          </a-form-model-item>
          <a-form-model-item :wrapper-col="{ span: 8, offset: 8 }">
            <a-button type="primary" @click="handleOk"> {{ $t('public.OK') }}  </a-button>
            <!-- <a-button :style="{ marginLeft: '10px' }"> {{$t('list.cancel')}} </a-button> -->
          </a-form-model-item>
        </a-form-model>
      </a-card>
    </a-col>
  </a-row>
</template>
<script>
import { getFileByBlobName } from '@/api/file'
import { mapGetters } from 'vuex'
import { uploadFile } from '@/api/file'
import { sendVerifyCode, changePhoneNumber} from '@/api/login'
export default {
  name: 'UserInfo',
  computed: {
    ...mapGetters(['organizationUnits']),
  },
  data() {
    return {
      getFileByBlobName: getFileByBlobName,
      form: Object.assign({},this.$store.getters.userInfo) ,
      user: JSON.parse(JSON.stringify(this.$store.getters.userInfo)), //深拷贝
      fileList: [],
      fileSignList: [],
      organizationUnitList: [],
      organizationUnit: '无',
      state: {
        time: 60,         
      },
      smsText: '获取验证码',
      smsButton: false,
      timer: null, //定时器对象
      rules: {
        name: [
          { required: true, message: '请输入', trigger: 'blur' },
          { max: 10, message: '最长10个字符', trigger: 'blur' },
        ],
        phoneNumber: [{ required: true, message: '请输入', trigger: 'blur' }],
        code: [{ required: true, message: '请输入', trigger: 'blur' }],
      },
    }
  },
  created() {},
  mounted() {
    const that = this
    var user = this.$store.getters.userInfo
    if (user.extraProperties.organizationUnits != null && user.extraProperties.organizationUnits.length > 0) {
      this.organizationUnit = user.extraProperties.organizationUnits.join('/')
    }
  },
  methods: {
    verifyPhoneNumber(value) {
      if (value == '') {
        return true
      }
      var phonereg = /^1[3456789]\d{9}$/
      if (value.length == 11 && phonereg.test(value)) {
        return true
      }
      return false
    },
    onSend() {
      const that = this
      if (!that.verifyPhoneNumber(that.form.phoneNumber)) {
        that.$message.error('手机号输入有误!')
        return
      }
      sendVerifyCode(that.form.phoneNumber, false).then((res) => {
        if (res.status) {
          that.$message.success('验证码已发送')                    
        } else {
          that.$message.error(res.message)
        }
      })
      that.smsButton=true
      that.timer = setInterval(() => {
        that.state.time--
        that.smsText = '获取验证码(' + that.state.time + '秒)'
        if (that.state.time <= 0) {
          that.smsButton = false
          that.smsText = '获取验证码'
          clearInterval(that.timer)
          that.state.time = 60
        }
      }, 1000)
    },
    beforeUpload(file) {
      this.fileList = [...this.fileList, file]
      return false
    },
    uploadChange(info) {
      const that = this
      const formData = new FormData()
      formData.append('file', info.file, info.file.name)
      if (info.file.size > 1024 * 20) {
        this.$message.error('头像不能大于 20KB当前：' + (info.file.size / 1024).toFixed(2) + 'KB')
        return
      }
      uploadFile(formData)
        .then((res) => {
          that.form.extraProperties.Avatar = res
        })
        .catch((err) => {
          that.$message.error('上传失败，请稍后再试。')
        })
    },
    beforeSignImgUpload(file) {
      this.fileSignList = [...this.fileSignList, file]
      return false
    },
    uploadSignImgChange(info) {
      const that = this
      const formData = new FormData()
      formData.append('file', info.file, info.file.name)
      if (info.file.size > 1024 * 5) {
        this.$message.error('签名不能大于 5KB当前：' + (info.file.size / 1024).toFixed(2) + 'KB')
        return
      }
      uploadFile(formData)
        .then((res) => {
          that.form.extraProperties.Signature = res
        })
        .catch((err) => {
          that.$message.error('上传失败，请稍后再试。')
        })
    },
    handleOk() {
      const that = this
      that.$refs.ruleForm.validate((valid) => {
        if (valid) {
          if (!that.verifyPhoneNumber(that.form.phoneNumber)) {
            that.$message.error('手机号输入有误!')
            return
          }
          const userData = {
            userName: that.form.userName,
            phoneNumber: that.form.phoneNumber,
            code: that.form.code,
            name: that.form.name,
          }
          changePhoneNumber(userData).then((res) => {
            if (res === 'ok') {
              that.$message.success('保存成功')
              that.form.code = ''
              that.$store.dispatch('GetInfo')
            } else {
              that.$message.error(res)
            }
          })
        } else return false
      })
    },
  },
}
</script>
